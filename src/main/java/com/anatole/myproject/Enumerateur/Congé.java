package com.anatole.myproject.Enumerateur;

public enum Congé {
    CongéAnnuel, //  un droit pour tous les salariés à prendre un nombre de jours de congé par an, rémunérés.

    CongéMaternitéPaternité, // un congé pour les femmes et les hommes à la naissance d'un enfant, permettant de s'occuper de l'enfant et de prendre soin de soi-même.

    CongéMaladie, // un congé pour les salariés qui sont malades ou qui ont été blessés au travail, pour leur permettre de récupérer et de se rétablir.

    CongéSansSolde, // un congé non rémunéré pour les salariés qui souhaitent prendre une pause dans leur travail pour des raisons personnelles ou familiales.

    CongéDeFormationProfessionnelle, // un congé pour les salariés qui souhaitent suivre une formation professionnelle pour améliorer leurs compétences.

    CongéDePaternité, // un congé pour les pères afin de leur permettre de passer du temps avec leur enfant nouvellement né.

    CongéMariage, // un congé pour les salariés qui se marient.

    CongéParental, // un congé pour les parents pour leur permettre de s'occuper de leur enfant.

    CongéDécès, // un congé pour les salariés qui ont perdu un membre de leur famille ou un proche.

    CongéSabbatique // un congé pour les salariés qui souhaitent prendre une pause dans leur carrière pour voyager, étudier ou se concentrer sur d'autres projets personnels.
}
